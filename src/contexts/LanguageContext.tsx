export const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LANGUAGE_OPTIONS: { code: Language; label: string; nativeLabel: string; dir: Direction }[] = [
  { code: 'en', label: 'English', nativeLabel: 'English', dir: 'ltr' },
  { code: 'ar', label: 'Arabic', nativeLabel: 'العربية', dir: 'rtl' },
  { code: 'ur', label: 'Urdu', nativeLabel: 'اردو', dir: 'rtl' },
  { code: 'hi', label: 'Hindi', nativeLabel: 'हिन्दी', dir: 'ltr' },
];

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    const saved = localStorage.getItem('language');
    return (saved as Language) || 'en';
  });

  const [dbOverrides, setDbOverrides] = useState<Map<string, string>>(new Map());
  const fetchIdRef = useRef(0);

  const direction: Direction = (language === 'ar' || language === 'ur') ? 'rtl' : 'ltr';

  const fetchDbTranslations = useCallback(async () => {
    const id = ++fetchIdRef.current;
    try {
      const { data } = await supabase
        .from('ui_translations' as any)
        .select('translation_key, translated_text')
        .eq('language_code', language);
      if (id === fetchIdRef.current && data) {
        const map = new Map<string, string>();
        (data as any[]).forEach(row => map.set(row.translation_key, row.translated_text));
        setDbOverrides(map);
      }
    } catch { /* silent */ }
  }, [language]);

  useEffect(() => {
    fetchDbTranslations();
  }, [fetchDbTranslations]);

  useEffect(() => {
    localStorage.setItem('language', language);
    document.documentElement.setAttribute('dir', direction);
    document.documentElement.setAttribute('lang', language);
  }, [language, direction]);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
  };

  const t = (key: string): string => {
    // DB overrides take precedence
    const dbVal = dbOverrides.get(key);
    if (dbVal) return dbVal;
    return translations[language]?.[key] || translations['en']?.[key] || key;
  };

  return (
    <LanguageContext.Provider value={{ language, direction, setLanguage, t, dbOverrides, refreshDbTranslations: fetchDbTranslations }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}
