// ============================================================
// GLOBAL FINANCE-EFFECT REGISTRY
// Enterprise ERP Finance Logic Engine
// Every document in the system is classified with full
// finance-effect metadata for posting, audit, commitment,
// budget control, and reporting consistency.
// ============================================================

export type FinancialClassification =
  | 'non_financial_master'
  | 'operational_non_posting'
  | 'commitment_document'
  | 'stock_movement_non_valued'
  | 'stock_movement_valued'
  | 'ar_financial_document'
  | 'ap_financial_document'
  | 'treasury_document'
  | 'manufacturing_cost_document'
  | 'project_cost_document'
  | 'adjustment_revaluation_document'
  | 'period_end_control_document'
  | 'control_approval_document'
  | 'analytical_document'
  | 'planning_document'
  | 'framework_commitment';

export type JournalTriggerPoint = 'on_add' | 'on_approval' | 'on_posting' | 'on_close' | 'on_certification' | 'configurable' | 'none';

export type CancellationMethod = 'direct_cancel' | 'credit_memo_required' | 'return_required' | 'reversal_journal' | 'close_only' | 'amendment_only' | 'not_applicable';

export type ReversalMethod = 'auto_reverse_journal' | 'manual_reverse_journal' | 'credit_note' | 'return_document' | 'adjustment_entry' | 'not_applicable';

export interface TypicalPosting {
  debit: string[];
  credit: string[];
  description: string;
}

export interface FinanceEffectDefinition {
  documentName: string;
  module: string;
  documentType: string;
  financialClassification: FinancialClassification;

  // Core finance flags
  createsJournalEntry: boolean;
  journalTriggerPoint: JournalTriggerPoint;
  updatesAR: boolean;
  updatesAP: boolean;
  updatesInventoryQty: boolean;
  updatesInventoryValue: boolean;
  updatesFixedAssets: boolean;
  updatesProjectBudgetCommitment: boolean;
  updatesProjectActualCost: boolean;
  updatesProjectRevenue: boolean;
  updatesCashFlowForecast: boolean;
  updatesTaxLedger: boolean;
  updatesRetentionBalance: boolean;
  updatesAdvanceBalance: boolean;
  updatesWIP: boolean;
  updatesBudgetConsumed: boolean;
  updatesCommitmentAmount: boolean;
  updatesSubcontractLiability: boolean;
  updatesCOGS: boolean;
  updatesDepreciation: boolean;

  // Posting logic
  typicalPostings?: TypicalPosting[];
  accountDeterminationSource: string[];
  postingPrerequisites: string[];

  // Approval & control
  approvalRequired: boolean;
  approvalConditions: string[];

  // Cancellation / reversal
  cancellationMethod: CancellationMethod;
  reversalMethod: ReversalMethod;
  reversalRules: string[];

  // Document flow
  upstreamDocuments: string[];
  downstreamDocuments: string[];

  // Validation
  validationRules: string[];

  // Dimension requirements
  dimensionRequirements: {
    branch: boolean;
    project: boolean;
    costCenter: boolean;
    costCode: boolean;
    activity: boolean;
    warehouse: boolean;
  };

  // Reporting
  reportingEffect: string[];

  // KPIs
  kpiImpact: string[];

  // Status flow
  statusFlow: string[];
}

