export function usePageHelpModule(): ModuleHelp | undefined {
  const location = useLocation();
  const path = location.pathname;

  // Direct match
  const helpId = routeToHelpMap[path];
  if (helpId) return getModuleById(helpId);

  // Try matching first segment
  const firstSegment = '/' + path.split('/').filter(Boolean)[0];
  const segmentHelpId = routeToHelpMap[firstSegment];
  if (segmentHelpId) return getModuleById(segmentHelpId);

  return undefined;
}

export function PageHelpButton() {
  const module = usePageHelpModule();
  if (!module) return null;
  return <ModuleHelpDrawer module={module} />;
}
